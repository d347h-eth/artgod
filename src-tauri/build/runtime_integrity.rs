use std::env;
use std::error::Error;
use std::fs::{self, File};
use std::io::{BufReader, Read, Write};
use std::path::{Path, PathBuf};

use sha2::{Digest, Sha256};

use crate::resource_contract::{
    BUNDLED_RUNTIME_RELATIVE_PATH, GENERATED_WALLET_RECIPIENT_INTEGRITY_FILE_NAME,
    WALLET_RECIPIENT_PROTECTED_ROOTS,
};

const CARGO_MANIFEST_DIR_ENV_KEY: &str = "CARGO_MANIFEST_DIR";
const CARGO_PROFILE_ENV_KEY: &str = "PROFILE";
const CARGO_RELEASE_PROFILE: &str = "release";

struct IntegrityEntry {
    relative_path: String,
    sha256: String,
}

/// Embeds deterministic hashes of every file that can execute in a release bot process.
pub(crate) fn generate_wallet_recipient_integrity_manifest(
    output_dir: &Path,
) -> Result<(), Box<dyn Error>> {
    let manifest_dir = PathBuf::from(env::var(CARGO_MANIFEST_DIR_ENV_KEY)?);
    let runtime_dir = manifest_dir.join(BUNDLED_RUNTIME_RELATIVE_PATH);
    let output_path = output_dir.join(GENERATED_WALLET_RECIPIENT_INTEGRITY_FILE_NAME);
    let release_build = env::var(CARGO_PROFILE_ENV_KEY)? == CARGO_RELEASE_PROFILE;

    println!("cargo:rerun-if-changed={}", runtime_dir.display());

    let entries = if release_build {
        collect_release_integrity_entries(&runtime_dir)?
    } else {
        Vec::new()
    };
    let generated = render_generated_manifest(&entries);
    let mut output = File::create(output_path)?;
    output.write_all(generated.as_bytes())?;
    Ok(())
}

fn collect_release_integrity_entries(
    runtime_dir: &Path,
) -> Result<Vec<IntegrityEntry>, Box<dyn Error>> {
    let runtime_dir = fs::canonicalize(runtime_dir).map_err(|error| {
        format!(
            "release runtime resources are unavailable at {}: {error}",
            runtime_dir.display()
        )
    })?;
    let mut files = Vec::<PathBuf>::new();

    for protected_root in WALLET_RECIPIENT_PROTECTED_ROOTS {
        let root_path = runtime_dir.join(protected_root);
        let before = files.len();
        collect_regular_files(&root_path, &mut files)?;
        if files.len() == before {
            return Err(format!(
                "wallet recipient integrity root has no files: {}",
                root_path.display()
            )
            .into());
        }
    }

    files.sort();
    files.dedup();
    let mut entries = Vec::with_capacity(files.len());
    for file_path in files {
        println!("cargo:rerun-if-changed={}", file_path.display());
        let relative_path = file_path
            .strip_prefix(&runtime_dir)?
            .to_str()
            .ok_or_else(|| format!("runtime path is not UTF-8: {}", file_path.display()))?
            .replace('\\', "/");
        entries.push(IntegrityEntry {
            relative_path,
            sha256: sha256_file(&file_path)?,
        });
    }
    Ok(entries)
}

fn collect_regular_files(path: &Path, files: &mut Vec<PathBuf>) -> Result<(), Box<dyn Error>> {
    let metadata = fs::symlink_metadata(path).map_err(|error| {
        format!(
            "wallet recipient integrity path is unavailable at {}: {error}",
            path.display()
        )
    })?;
    if metadata.file_type().is_symlink() {
        return Err(format!(
            "wallet recipient integrity path must not be a symbolic link: {}",
            path.display()
        )
        .into());
    }
    if metadata.is_file() {
        files.push(fs::canonicalize(path)?);
        return Ok(());
    }
    if !metadata.is_dir() {
        return Err(format!(
            "wallet recipient integrity path is not a file or directory: {}",
            path.display()
        )
        .into());
    }

    let mut entries = fs::read_dir(path)?.collect::<Result<Vec<_>, _>>()?;
    entries.sort_by_key(|entry| entry.file_name());
    for entry in entries {
        collect_regular_files(&entry.path(), files)?;
    }
    Ok(())
}

fn sha256_file(path: &Path) -> Result<String, Box<dyn Error>> {
    let mut reader = BufReader::new(File::open(path)?);
    let mut hasher = Sha256::new();
    let mut buffer = [0_u8; 64 * 1024];
    loop {
        let bytes_read = reader.read(&mut buffer)?;
        if bytes_read == 0 {
            break;
        }
        hasher.update(&buffer[..bytes_read]);
    }
    Ok(format!("{:x}", hasher.finalize()))
}

fn render_generated_manifest(entries: &[IntegrityEntry]) -> String {
    let mut output = String::from(
        "// @generated by src-tauri/build/runtime_integrity.rs; do not edit.\n\
         #[allow(dead_code)]\n\
         const EMBEDDED_WALLET_RECIPIENT_RUNTIME_FILES: &[PinnedRuntimeFile<'static>] = &[\n",
    );
    for entry in entries {
        output.push_str("    PinnedRuntimeFile { relative_path: ");
        output.push_str(&format!("{:?}", entry.relative_path));
        output.push_str(", sha256: ");
        output.push_str(&format!("{:?}", entry.sha256));
        output.push_str(" },\n");
    }
    output.push_str("];\n");
    output
}
